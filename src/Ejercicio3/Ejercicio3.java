    private void cmdRestaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdRestaActionPerformed
        int r;
        r = Integer.parseInt (txtLongitud1.getText ().trim());
        resta = new double [r];

        for (int i = 0; i < x.length; i++) {
            resta[i]=(x[i]-y[i]);
        }
        for (int i = 0; i < resta.length; i++) {
            txtResultado.append("El resultado de la resta en la posición "+i+" es: "+resta[i]+"\n");

        }
    }//GEN-LAST:event_cmdRestaActionPerformed

