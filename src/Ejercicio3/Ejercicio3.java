    private void cmdAutomaticoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdAutomaticoActionPerformed
        double num,n1,n2;

        num=Double.parseDouble(JOptionPane.showInputDialog(this,"Digite el número hasta donde quiere el llenado: "));
        while(num<x.length){
            JOptionPane.showMessageDialog(this, "Digite un número mayor a: "+x.length);
            num=Double.parseDouble(JOptionPane.showInputDialog(this,"Digite el número hasta donde quiere el llenado: "));
        }
        for (int i = 0; i < x.length; i++) {
            n1=(int)(Math.random()*num+1);
            x[i]=n1;
        }
        while(num<y.length){
            JOptionPane.showMessageDialog(this, "Digite un número mayor a: "+y.length);
            num=Double.parseDouble(JOptionPane.showInputDialog(this,"Digite el número hasta donde quiere el llenado: "));
        }
        for (int i = 0; i < y.length; i++) {
            n2=(int)(Math.random()*num+1);
            y[i]=n2;
        }
        JOptionPane.showMessageDialog(this, "Vectores Llenados Correctamente");
    }//GEN-LAST:event_cmdAutomaticoActionPerformed

