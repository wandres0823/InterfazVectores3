    private void cmdCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdCrearActionPerformed
        int long1,long2;
        if(txtLongitud1.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "Digite la cantidad  1.","ERROR",JOptionPane.ERROR_MESSAGE);
            txtLongitud1.requestFocusInWindow();
        }else if(txtLongitud2.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(this, "Digite la cantidad 2.", "ERROR", JOptionPane.ERROR_MESSAGE);
            txtLongitud2.requestFocusInWindow();
        }else if(Integer.parseInt(txtLongitud1.getText().trim())==0){
            JOptionPane.showMessageDialog(this, "La cantidad no puede ser cero.","ERROR",JOptionPane.ERROR_MESSAGE);
            txtLongitud1.requestFocusInWindow();
            txtLongitud1.selectAll();
        }else if(Integer.parseInt(txtLongitud2.getText().trim())==0){
            JOptionPane.showMessageDialog(this, "La cantidad no puede ser cero.","ERROR",JOptionPane.ERROR_MESSAGE);
            txtLongitud2.requestFocusInWindow();
            txtLongitud2.selectAll();
        }else{
            long1=Integer.parseInt(txtLongitud1.getText());
            long2=Integer.parseInt(txtLongitud2.getText());
            x = new double[long1];
            y = new double[long2];
            JOptionPane.showMessageDialog(this, "Los vectores Se Crearon Exitosamente");
        }
    }//GEN-LAST:event_cmdCrearActionPerformed

